#!/usr/bin/env node

import { readFile, mkdir, rm, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const skillDir = dirname(fileURLToPath(import.meta.url));
const contractPath = resolve(skillDir, "../contract/openapi.yaml");
const outputDir = resolve(skillDir, "api-reference");
const source = await readFile(contractPath, "utf8");
const lines = source.replaceAll("\r\n", "\n").split("\n");
const methods = new Set(["get", "post", "put", "patch", "delete", "head", "options"]);

const indentOf = (line) => line.length - line.trimStart().length;
const unquote = (value) => {
  const trimmed = value.trim();
  if ((trimmed.startsWith('"') && trimmed.endsWith('"')) ||
      (trimmed.startsWith("'") && trimmed.endsWith("'"))) {
    return trimmed.slice(1, -1);
  }
  return trimmed;
};

function scalar(block, key) {
  const prefix = `${key}:`;
  const index = block.findIndex((line) => line.trimStart().startsWith(prefix));
  if (index < 0) return "";
  const line = block[index];
  const value = line.trimStart().slice(prefix.length).trim();
  if (value && value !== ">" && value !== "|") return unquote(value);

  const baseIndent = indentOf(line);
  const continuation = [];
  for (let i = index + 1; i < block.length && indentOf(block[i]) > baseIndent; i += 1) {
    continuation.push(block[i].trim());
  }
  return continuation.join(value === "|" ? "\n" : " ").trim();
}

function schemaSnippet(block) {
  const index = block.findIndex((line) => /^\s{12}schema:/.test(line));
  if (index < 0) return "None";
  const baseIndent = indentOf(block[index]);
  const snippet = [block[index].slice(baseIndent)];
  for (let i = index + 1; i < block.length; i += 1) {
    if (block[i].trim() && indentOf(block[i]) <= baseIndent) break;
    snippet.push(block[i].slice(Math.min(baseIndent, indentOf(block[i]))));
  }
  return `\`\`\`yaml\n${snippet.join("\n").trimEnd()}\n\`\`\``;
}

function parameterNames(block) {
  const text = block.join("\n");
  const names = new Set();
  for (const match of text.matchAll(/#\/components\/parameters\/([A-Za-z0-9_-]+)/g)) {
    names.add(match[1]);
  }
  for (const match of text.matchAll(/(?:^|[{,])\s*name:\s*([A-Za-z0-9_-]+)/gm)) {
    names.add(match[1]);
  }
  return [...names];
}

function responseRows(block) {
  const rows = [];
  for (let i = 0; i < block.length; i += 1) {
    const match = block[i].match(/^\s{8}"([0-9]{3})":(?:\s*(.*))?$/);
    if (!match) continue;
    const code = match[1];
    const inline = match[2] ?? "";
    let detail = inline.includes("$ref") ? "OperationResult" : "";
    for (let j = i + 1; j < block.length && indentOf(block[j]) > 8; j += 1) {
      if (block[j].trimStart().startsWith("description:")) {
        detail = scalar(block.slice(j), "description");
        break;
      }
    }
    rows.push([code, detail || "See response schema in the contract"]);
  }
  return rows;
}

const endpoints = [];
let currentPath = "";
for (let i = 0; i < lines.length; i += 1) {
  const pathMatch = lines[i].match(/^  (\/[^:]+):\s*$/);
  if (pathMatch) {
    currentPath = pathMatch[1];
    continue;
  }
  const methodMatch = lines[i].match(/^    ([a-z]+):\s*$/);
  if (!currentPath || !methodMatch || !methods.has(methodMatch[1])) continue;

  let end = i + 1;
  while (end < lines.length && !/^  \/[^:]+:\s*$/.test(lines[end]) && !/^    [a-z]+:\s*$/.test(lines[end])) {
    end += 1;
  }
  const block = lines.slice(i + 1, end);
  endpoints.push({
    path: currentPath,
    method: methodMatch[1].toUpperCase(),
    operationId: scalar(block, "operationId"),
    summary: scalar(block, "summary") || scalar(block, "description"),
    snapshot: scalar(block, "x-snapshot"),
    parameters: parameterNames(block),
    request: schemaSnippet(block),
    responses: responseRows(block),
  });
  i = end - 1;
}

const out = [];
out.push("# Paperclips in the Dark API reference", "");
out.push("> Generated from `contract/openapi.yaml` by `skill/generate-api-reference.mjs`. Do not edit this file by hand.", "");
out.push(`Base URL: \`http://localhost:9657/api\``, "");
out.push(`Operations: ${endpoints.length}`, "");
out.push("## Conventions", "");
out.push("- Mutations use JSON `POST` requests and return the uniform `OperationResult` described by the contract.");
out.push("- `ifMatch` means the optional `If-Match` revision header; `idempotencyKey` means the optional `Idempotency-Key` header.");
out.push("- A snapshot value reports the contract's `x-snapshot` setting for that mutation.", "");
out.push("## Endpoint index", "");
out.push("| Method | Path | Operation | Snapshot |", "| --- | --- | --- | --- |");
for (const endpoint of endpoints) {
  out.push(`| ${endpoint.method} | \`${endpoint.path}\` | [\`${endpoint.operationId}\`](#${endpoint.operationId.toLowerCase()}) | ${endpoint.snapshot || "—"} |`);
}

for (const endpoint of endpoints) {
  out.push("", `## ${endpoint.operationId}`, "");
  out.push(`\`${endpoint.method} ${endpoint.path}\``);
  if (endpoint.summary) out.push("", endpoint.summary);
  out.push("", `Parameters: ${endpoint.parameters.length ? endpoint.parameters.map((name) => `\`${name}\``).join(", ") : "none"}`);
  if (endpoint.snapshot) out.push("", `Snapshot: \`${endpoint.snapshot}\``);
  out.push("", "Request body schema:", "", endpoint.request);
  out.push("", "Responses:", "");
  if (endpoint.responses.length) {
    for (const [code, detail] of endpoint.responses) out.push(`- \`${code}\`: ${detail}`);
  } else {
    out.push("- See the frozen OpenAPI contract.");
  }
}

await rm(outputDir, { recursive: true, force: true });
await mkdir(outputDir, { recursive: true });
await writeFile(resolve(outputDir, "README.md"), `${out.join("\n")}\n`, "utf8");

